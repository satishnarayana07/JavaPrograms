package ArrayPack;

public class Matrix {

	public static void main(String[] args) {
		
	int x[][]= {{1,2,3,4},{4,6,8,9},{3,5,3,4}};
	
	System.out.println("The 2d array elements are:");
	
	for(int i=0;i<3;i++) {
		
		for(int j=0;j<4;j++) {
			
			System.out.print(x[i][j]+"\t");
		}
		
		System.out.println();
	}
	
	
	

	}

}
